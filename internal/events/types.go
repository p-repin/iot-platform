// Package events — Event Sourcing для прослеживаемости деталей.
//
// ЗАЧЕМ Event Sourcing в IoT-платформе?
// В машиностроении и космической отрасли требуется ПОЛНАЯ история каждой детали:
// - Когда создана, из какого материала, из какой партии
// - На каких станках обрабатывалась, сколько длилась каждая операция
// - Какие результаты дала ML-модель (обнаружены ли аномалии)
// - Когда отгружена, кто провёл финальный контроль
//
// CRUD-подход теряет историю: UPDATE parts SET status = 'shipped' — и всё,
// предыдущие состояния стёрты. Event Sourcing сохраняет КАЖДОЕ событие,
// и текущее состояние восстанавливается "проигрыванием" всех событий.
//
// На собеседовании: "Как вы обеспечиваете прослеживаемость деталей?"
// Ответ: Event Sourcing — append-only журнал событий + агрегат с восстановлением состояния.
package events

import "time"

// ============================================================
// Типы событий
// ============================================================

// EventType — тип события в Event Sourcing журнале.
//
// ПОЧЕМУ string, а не iota (int)?
// 1. Читаемость в БД: в таблице events видно "PartCreated", а не "1"
// 2. Расширяемость: новый тип — просто новая константа, без риска коллизий
// 3. Совместимость: при десериализации из JSONB строка маппится однозначно
// 4. Дебаг: в логах и алертах "PartShipped" понятнее чем "4"
type EventType string

const (
	// PartCreated — деталь создана (начало производства).
	// Первое событие в жизненном цикле детали.
	PartCreated EventType = "PartCreated"

	// PartMachined — деталь обработана на станке.
	// Может быть несколько: фрезерование → токарка → сверление.
	PartMachined EventType = "PartMachined"

	// InferenceCompleted — ML-модель проанализировала данные станка.
	// Записывается при обнаружении аномалии (score > threshold).
	InferenceCompleted EventType = "InferenceCompleted"

	// PartShipped — деталь отгружена заказчику.
	// Финальное событие в жизненном цикле.
	PartShipped EventType = "PartShipped"
)

// ============================================================
// DomainEvent — доменное событие
// ============================================================

// DomainEvent — одно событие в Event Sourcing журнале.
//
// ПОЧЕМУ отдельный struct, а не интерфейс?
// В Go интерфейс для событий создаёт лишнюю сложность:
// нужны type assertions при каждом чтении, нельзя легко сериализовать.
// Struct с полем Payload (any) проще: один тип для хранения и передачи,
// типизация payload через конкретные *Payload-структуры.
//
// EventID — автоинкрементный ID из БД (BIGSERIAL).
// ЗАЧЕМ? Гарантирует глобальный порядок событий.
// UUID не упорядочен — нельзя сказать какое событие было раньше.
// При восстановлении состояния порядок критически важен:
// PartCreated ДОЛЖЕН идти раньше PartMachined.
type DomainEvent struct {
	EventID     int64     // глобальный порядковый номер (из БД)
	EventType   EventType // тип события
	AggregateID string    // UUID агрегата (детали)
	Payload     any       // типизированный payload (*PartCreatedPayload и т.д.)
	CreatedAt   time.Time // время события
}

// ============================================================
// Payload-структуры для каждого типа события
// ============================================================
// ПОЧЕМУ отдельные структуры, а не map[string]any?
// 1. Типобезопасность: компилятор проверяет поля, IDE подсказывает
// 2. Документация: поля и их типы видны в коде
// 3. Валидация: можно добавить проверки в конструкторы
// 4. Производительность: struct быстрее чем map + type assertion

// PartCreatedPayload — данные о создании детали.
//
// Фиксирует начальные параметры: из чего и в рамках какой партии.
// При расследовании брака можно найти ВСЕ детали из той же партии
// материала и проверить их.
type PartCreatedPayload struct {
	PartNumber string `json:"part_number"` // номер детали ("P-00a1b2c3")
	Material   string `json:"material"`    // материал ("steel", "aluminum", "titanium")
	BatchID    string `json:"batch_id"`    // ID партии материала ("B-42")
}

// PartMachinedPayload — данные об обработке детали на станке.
//
// Одна деталь может пройти несколько обработок:
// CNC-01 (фрезерование, 120с) → LATHE-01 (токарка, 90с) → MILL-01 (шлифовка, 60с).
// Каждая операция — отдельное событие PartMachined.
type PartMachinedPayload struct {
	MachineID   string  `json:"machine_id"`   // ID станка ("CNC-01")
	Operation   string  `json:"operation"`    // тип операции ("milling", "turning", "drilling")
	DurationSec float64 `json:"duration_sec"` // длительность обработки в секундах
}

// InferenceCompletedPayload — результат ML-инференса.
//
// Записывается когда ML-модель проанализировала показания станка
// во время обработки детали. Score > threshold = аномалия.
// При расследовании брака можно увидеть:
// "ML-модель дала score 0.87 на станке CNC-07 в 10:15:30"
type InferenceCompletedPayload struct {
	MachineID string  `json:"machine_id"` // на каком станке
	Score     float64 `json:"score"`      // anomaly score (0..1)
	IsAnomaly bool    `json:"is_anomaly"` // score > threshold
	Model     string  `json:"model"`      // версия модели ("anomaly_detector_v1")
}

// PartShippedPayload — данные об отгрузке детали.
//
// Фиксирует финальный контроль и отгрузку.
// Passed=false означает что деталь не прошла контроль — брак.
// В этом случае можно "проиграть" все события детали и найти
// на каком этапе что-то пошло не так.
type PartShippedPayload struct {
	Destination string `json:"destination"`  // куда отгружена ("Склад-А", "Заказчик-Б")
	InspectorID string `json:"inspector_id"` // кто проверил ("inspector-42")
	Passed      bool   `json:"passed"`       // прошла ли финальный контроль
}
