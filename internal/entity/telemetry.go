// Package entity — доменные сущности платформы.
//
// Это ЯДРО чистой архитектуры. Сущности не зависят ни от чего:
// ни от базы данных, ни от protobuf, ни от транспорта.
//
// ПОЧЕМУ отдельный пакет?
// В Clean Architecture сущности — самый внутренний слой.
// Все остальные слои зависят от entity, но entity не зависит ни от кого.
// Это гарантирует что бизнес-модель не "протекает" наружу:
// - Замена protobuf на JSON? entity не меняется.
// - Замена PostgreSQL на MongoDB? entity не меняется.
// - Добавление REST API рядом с gRPC? entity не меняется.
//
// ПРАВИЛО: если ты добавляешь import в этот пакет — подумай дважды.
// Допустимы ТОЛЬКО стандартные библиотеки Go (time, errors и т.д.).
package entity

import "time"

// Record — одно показание с одного станка в один момент времени.
//
// Это атомарная единица данных в системе. Один станок, один момент,
// набор показаний датчиков.
//
// ПОЧЕМУ time.Time, а не int64/protobuf.Timestamp?
// time.Time — стандартный тип Go для работы с временем.
// Protobuf Timestamp — это транспортный формат (для сериализации по сети).
// В бизнес-логике мы работаем с time.Time, конвертация pb↔time
// происходит на границе транспортного слоя.
type Record struct {
	// RecordID — уникальный идентификатор записи (UUID).
	// Генерируется агентом ДО отправки. Используется для идемпотентности:
	// при повторной отправке (at-least-once) сервер делает upsert по этому ID.
	RecordID string

	// MachineID — идентификатор станка ("CNC-01", "LATHE-02").
	MachineID string

	// Timestamp — время снятия показания.
	Timestamp time.Time

	// Показания датчиков.
	Temperature float64 // Температура (°C)
	Vibration   float64 // Вибрация (мм/с)
	Pressure    float64 // Давление (бар)
}

// Batch — пакет записей от одного агента.
//
// ПОЧЕМУ батч, а не отдельные записи?
// Батч позволяет:
// 1. Записать всё в БД одной транзакцией (атомарность)
// 2. Уменьшить сетевой overhead (один RPC вместо N)
// 3. Упростить backpressure (считаем батчи, а не записи)
type Batch struct {
	Records []Record
	AgentID string
}

// RecordError — ошибка обработки конкретной записи.
//
// Позволяет возвращать частичный результат: из 100 записей
// 98 приняты, 2 с ошибками. Агент перешлёт только проблемные.
type RecordError struct {
	RecordID string
	Message  string
}
