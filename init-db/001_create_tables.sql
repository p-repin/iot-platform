-- 001_create_tables.sql
-- Скрипты в docker-entrypoint-initdb.d выполняются ПО АЛФАВИТУ при ПЕРВОМ запуске.
-- Префикс 001_ гарантирует порядок выполнения.

-- ============================================================
-- Расширение TimescaleDB
-- ============================================================
-- БЕЗ этой строки hypertable создать нельзя — это расширение PostgreSQL
CREATE EXTENSION IF NOT EXISTS timescaledb;

-- ============================================================
-- Таблица телеметрии — основное хранилище данных со станков
-- ============================================================
-- ПОЧЕМУ отдельные колонки (temperature, vibration, pressure), а не JSONB?
-- 1. Типизация: БД гарантирует что temperature — это число, а не строка
-- 2. Индексы: можно создать индекс по конкретному параметру
-- 3. Сжатие: TimescaleDB сжимает колонки независимо — однородные данные
--    сжимаются лучше чем JSONB-месиво
-- 4. Запросы: WHERE temperature > 100 работает напрямую, без распаковки JSON
--
-- КОГДА JSONB лучше?
-- Если набор параметров у каждого станка разный и заранее неизвестен.
-- У нас параметры стандартизированы через protobuf-схему — колонки подходят.
CREATE TABLE IF NOT EXISTS telemetry (
    -- Время записи показания. Это PRIMARY измерение для временных рядов.
    time        TIMESTAMPTZ NOT NULL,

    -- Идентификатор станка. TEXT а не INT — станки могут иметь
    -- буквенно-цифровые ID вроде "CNC-07" или "LATHE-12"
    machine_id  TEXT        NOT NULL,

    -- Параметры телеметрии (расширяемый набор)
    temperature DOUBLE PRECISION,
    vibration   DOUBLE PRECISION,
    pressure    DOUBLE PRECISION,

    -- Уникальный ID записи для идемпотентности.
    -- ПОЧЕМУ UUID, а не SERIAL?
    -- - Агент генерирует ID на своей стороне ДО отправки
    -- - При повторной отправке (at-least-once) тот же ID → upsert, а не дубликат
    -- - SERIAL генерируется на стороне БД — при повторной вставке будет новый ID
    record_id   UUID        NOT NULL,

    -- ВАЖНО: TimescaleDB требует, чтобы колонка партиционирования (time)
    -- входила во ВСЕ UNIQUE constraints таблицы.
    -- Это ограничение hypertable: каждый чанк — отдельная партиция,
    -- и уникальность можно гарантировать только внутри чанка (по time).
    -- Поэтому UNIQUE(record_id) невозможен — нужен UNIQUE(record_id, time).
    -- На практике record_id (UUID) и так уникален, time — формальность.
    UNIQUE (record_id, time)
);

-- ============================================================
-- Превращаем обычную таблицу в hypertable
-- ============================================================
-- ЗАЧЕМ hypertable?
-- Под капотом TimescaleDB разбивает таблицу на чанки (партиции) по времени.
-- Запрос "данные за последний час" читает ОДИН чанк, а не сканирует всю таблицу.
-- Старые чанки можно сжимать или удалять без влияния на свежие данные.
--
-- ПАРАМЕТР chunk_time_interval:
-- '1 day' — один чанк = один день данных.
-- - Слишком мелко (1 час): много чанков, больше overhead на планирование запросов
-- - Слишком крупно (1 месяц): чанки большие, сжатие/удаление грубое
-- - 1 день — разумный баланс для ~20 станков с записью раз в секунду
SELECT create_hypertable(
    'telemetry',
    'time',
    chunk_time_interval => INTERVAL '1 day',
    -- if_not_exists: при повторном запуске скрипт не упадёт
    if_not_exists => TRUE
);

-- ============================================================
-- Индекс для быстрого поиска по станку + времени
-- ============================================================
-- ПОЧЕМУ составной индекс (machine_id, time DESC), а не два отдельных?
-- Типичный запрос: "последние 100 записей станка CNC-07"
-- Составной индекс отвечает на него ОДНИМ проходом по B-tree
-- Два отдельных индекса потребовали бы bitmap merge — медленнее
CREATE INDEX IF NOT EXISTS idx_telemetry_machine_time
    ON telemetry (machine_id, time DESC);

-- ============================================================
-- Таблица событий — Event Sourcing для прослеживаемости деталей
-- ============================================================
-- ПОЧЕМУ Event Sourcing, а не обычный CRUD?
-- CRUD: UPDATE parts SET status = 'shipped' — предыдущее состояние ПОТЕРЯНО
-- Event Sourcing: записываем КАЖДОЕ событие — полная аудиторская история
-- "Деталь X была создана в 10:00, обработана на станке CNC-07 в 10:15,
--  ML-модель дала вероятность брака 2% в 10:16, отгружена в 10:30"
--
-- Это критически важно в машиностроении/космосе:
-- если деталь вышла из строя, нужно восстановить ВСЮ историю её производства
CREATE TABLE IF NOT EXISTS events (
    -- Автоинкрементный ID события — глобальный порядок
    -- ПОЧЕМУ SERIAL, а не UUID?
    -- В event sourcing важен ПОРЯДОК событий. SERIAL гарантирует монотонный рост.
    -- UUID не упорядочен — нельзя сказать какое событие было раньше.
    event_id    BIGSERIAL   PRIMARY KEY,

    -- Тип события: "PartCreated", "PartMachined", "InferenceCompleted", "PartShipped"
    event_type  TEXT        NOT NULL,

    -- ID агрегата (детали). Все события одной детали связаны этим полем.
    -- По нему мы "проигрываем" события чтобы восстановить текущее состояние.
    aggregate_id UUID       NOT NULL,

    -- Полезная нагрузка события в формате JSONB
    -- ПОЧЕМУ здесь JSONB, а не колонки?
    -- У каждого типа события РАЗНАЯ структура данных:
    -- PartCreated: {material: "steel", batch: "B-42"}
    -- PartMachined: {machine_id: "CNC-07", duration_sec: 120}
    -- Колонки потребовали бы добавлять поля для каждого нового типа события
    payload     JSONB       NOT NULL DEFAULT '{}',

    -- Время записи события
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Индекс для восстановления истории детали:
-- SELECT * FROM events WHERE aggregate_id = '...' ORDER BY event_id
CREATE INDEX IF NOT EXISTS idx_events_aggregate
    ON events (aggregate_id, event_id);

-- ============================================================
-- Таблица алертов — сохраняем критичные уведомления
-- ============================================================
-- ПОЧЕМУ отдельная таблица, а не только Redis pub/sub?
-- Redis pub/sub = fire-and-forget: если никто не слушает, сообщение потеряно.
-- Алерты CRITICAL должны быть сохранены и показаны оператору даже если
-- он открыл дашборд через час после события.
CREATE TABLE IF NOT EXISTS alerts (
    alert_id    BIGSERIAL   PRIMARY KEY,
    machine_id  TEXT        NOT NULL,

    -- Три уровня критичности:
    -- 'info'     — информационный (параметр приближается к границе)
    -- 'warning'  — предупреждение (параметр вышел за soft limit)
    -- 'critical' — критический (требуется остановка / вмешательство оператора)
    severity    TEXT        NOT NULL CHECK (severity IN ('info', 'warning', 'critical')),

    message     TEXT        NOT NULL,
    resolved    BOOLEAN     NOT NULL DEFAULT FALSE,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    resolved_at TIMESTAMPTZ
);

-- Индекс для дашборда: "покажи все нерешённые алерты, сначала критичные"
CREATE INDEX IF NOT EXISTS idx_alerts_unresolved
    ON alerts (resolved, severity, created_at DESC);